public class Operadores{
    public static void main(String[] args) {
    int numero = 5;
    
    System.out.println("Exemplo de adicao ou incremento no " + numero + " em 3 = " + (numero += 3));
    System.out.println("Exemplo de subtracao ou decremento no " + numero + " em 2 = " + (numero -= 2));
    System.out.println("Exemplo de multiplicacao no " + numero + " por 4 = " + (numero*4));
    System.out.println("Exemplo de divisao no " + numero + " por 2 = " + (numero/2));
    }    
}

public class PrecedenciaOperadoresAtribuicao {
    public static void main(String[] args) {
        int numero = 5; 

        System.out.println("Exemplo de pre incremento ++var no " + numero + " = " + (++numero));
        System.out.println("Exemplo de pos incremento var++ no " + numero + " = " + (numero++));
        System.out.println("Exemplo de pre decremento --var no " + numero + " = " + (--numero));
        System.out.println("Exemplo de pos decremento var-- no " + numero + " = " + (numero--));


    }
    
}

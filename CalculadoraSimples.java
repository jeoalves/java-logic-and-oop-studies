public class CalculadoraSimples {
    public static void main (String[]args){
        int a = 10;
        int b = 2;

        System.out.println("A soma de a + b = " + (a+b));
        System.out.println("A subtracao de a - b = " + (a-b));
        System.out.println("A multiplicacao de a x b = " + (a*b));
        System.out.println("A divisao de a e b = " + (a/b));
        System.out.println("O resto da divisao entre a e b = " + (a%b));
    }    
}
